		<strong>Câu 1</strong>: Phát biểu nào sau đây là đúng khi nói về môi trường sống của sinh vật?
	</p>
	<p>
		<strong>A.</strong> Môi trường là nơi sống của sinh vật, bao gồm tất cả các nhân tố vô sinh và hữu
		sinh ở xung quanh sinh vật, có tác  động trực tiếp, gián tiếp tới sinh vật.
	</p>
	<p>
		<strong>*B.</strong> Môi trường bao gồm tất cả các nhân tố ở xung quanh sinh vật, có tác động trực
		tiếp, gián tiếp tới sinh vật, làm ảnh  hưởng tới sự tồn tại, sinh trưởng, phát triển và những hoạt
		động khác của sinh vật.
	</p>
	<p>
		<strong>C.</strong> Môi trường là nơi sống của sinh vật, bao gồm tất cả các nhân tố vô sinh xung
		quanh sinh vật.
	</p>
	<p>
		<strong>D.</strong> Môi trường là nơi sống của sinh vật, bao gồm tất cả các nhân tố hữu sinh xung
		quanh sinh vật.
